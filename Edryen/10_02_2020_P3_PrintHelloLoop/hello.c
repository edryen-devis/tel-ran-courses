#include<stdio.h>
void printHello(int num);
void printH(int num);
void printH1(int num);

int main(){
	printHello(1);
	printH(2);
	printH1(13);
	return 0;
}
void printHello(int num){
	int i;
	for (i=0; i<=num; i++){
		printf("%d Hello!\n", i);

	}
}
void printH(int num){
	int i=0;
	do{
			printf(" %d Hello!\n", i);
			i++;

		}while(i<=num);
}

void printH1(int num){
	while(num!=0){
		printf("%d Hello!\n", num);
		num--;
	}
}
